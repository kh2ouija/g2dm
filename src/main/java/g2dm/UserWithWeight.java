public class UserWithWeight {

    private String user;
    private Double weight;

    public UserWithWeight(String user, Double weight) {
        this.user = user;
        this.weight = weight;
    }

    public String getUser() {
        return user;
    }

    public Double getWeight() {
        return weight;
    }
}
